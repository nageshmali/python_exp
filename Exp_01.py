def area_of_circle(r):
    area = 3.14 * r * r
    print("Area of Circle = ",area)

def area_of_rect(l,b):
    area = l * b;
    print("Area of Rectangle = ",area)

def area_of_triangle(b,h):
    area = 0.5 * b * h
    print("Area of Triangle - ",area)

    
area_of_circle(5)
area_of_rect(10,20)
area_of_triangle(10,30)